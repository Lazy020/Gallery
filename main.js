function openImage(element) {
    const modal = document.getElementById('modal');
    const modalImage = document.getElementById('modal-image');
  
    modalImage.src = element.src;
    modal.style.display = 'block';
  }
  
  function closeImage() {
    const modal = document.getElementById('modal');
    modal.style.display = 'none';
  }
